#include <stdlib.h>

/**
 * カンマを飛ばしてインデックス進める関数
*/
void	skip_comma(size_t *index, const char *const str)
{
	while (str[*index] != '\0'
		&& str[*index] != '\n'
		&& str[*index] == ',')
	{
		(*index)++;
	}
}
